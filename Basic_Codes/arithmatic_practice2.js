var base = parseInt(prompt("Enter base : "));
var height = parseInt(prompt("Enter height : "));

document.write("Base = "+base +"<br/>");
document.write("Height = "+height +"<br/><br/>");

var area = base * height ;
document.write("Rectangle Area = "+area +"<br/>");

var area = 0.5 * base * height ;
document.write("Triangle Area = "+area +"<br/>");
