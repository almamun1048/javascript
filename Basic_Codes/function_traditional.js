//Traditional Function


//creating a function for square
function square(num1){
    var result = num1 * num1;
    return result;
}
//creating a function for add
function add(num1,num2){
    var result = num1 + num2;
    return result;
}
//creating a function for square
function sub(num1,num2){
    var result = num1 - num2;
    return result;
}

//calling function
var x = square(5);
document.write(x+"<br/>");

var x = add(5,6);
document.write(x+"<br/>");

var x = sub(15,5);
document.write(x+"<br/>");
