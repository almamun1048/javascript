// Relational Operator =>   >,>=,<,<=,==,===,!=,!==

//Logical Operator => &&,||,!

var num1 = 10,num2 = 5,num3 = 25,num4 = 15;


//check num1 greater than num2
console.log(num1>num2);

//check num1 greater than or equal num2
console.log(num1>=num2);

//check num1 less than num2
console.log(num1<num2);

//check num1 less than or equal num2
console.log(num1<=num2);

//check num1 equal to num2 without datatype
console.log(num1==num2);

//check num1 equal to num2 with datatype
console.log(num1===num2);

//check num1 not equal to num2 without datatype
console.log(num1!=num2);

//check num1 not equal to num2 with datatype
console.log(num1!==num2);

//&& if all condition become true = true
console.log(num1>num2 && num3>num4);

//|| if one condition become true = true
console.log(num1>num2 || num3>num4);